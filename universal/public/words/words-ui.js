// ì–¸ì–´íŒ© ë™ì  ë¡œë”©
function loadWordPack(nativeLang, targetLang) {
    const key = `${nativeLang}-${targetLang}`;

    // ì´ë¯¸ ë¡œë”©ë¨
    if (loadedWordPacks[key]) {
        return Promise.resolve(languageData.wordPacks[nativeLang][targetLang]);
    }

    return new Promise((resolve, reject) => {
        const script = document.createElement('script');
        script.src = `words/${nativeLang}-${targetLang}.js`;
        script.onload = () => {
            loadedWordPacks[key] = true;
            resolve(languageData.wordPacks[nativeLang][targetLang]);
        };
        script.onerror = () => {
            console.error(`Failed to load word pack: ${key}`);
            reject(new Error(`Failed to load word pack: ${key}`));
        };
        document.head.appendChild(script);
    });
}

