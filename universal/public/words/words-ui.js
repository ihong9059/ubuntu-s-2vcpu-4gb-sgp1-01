function getAvailableCombinations() {
    const langs = ['ko', 'en', 'ja', 'zh', 'es', 'fr', 'de'];
    const combinations = [];
    for (const native of langs) {
        for (const target of langs) {
            if (native !== target) {
                combinations.push({
                    native: native,
                    target: target,
                    nativeName: languageData.languages[native].name,
                    targetName: languageData.languages[target].name,
                    nativeFlag: languageData.languages[native].flag,
                    targetFlag: languageData.languages[target].flag
                });
            }
        }
    }
    return combinations;
}

